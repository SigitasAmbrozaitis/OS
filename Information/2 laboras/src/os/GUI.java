    public void InputToDisplay(){
        
        String input = InputDevice.getText();
        String[] words = new String[OS.realMachine.getSZ()];
        String word = "";
        int size = 0;
        
        for( int i = 0 ; i < input.length() ; i++ ){
            System.out.println(input.length());
            if( input.charAt(i) == ' ' ){  
                if( word.length() <= 5 ){
                    words[size] = word;
                    size++;
                    word = "";
                }
            }
            else{               
                word += input.charAt(i);
                if( word.length() == 5 ){
                    words[size] = word;
                    size++;
                    word = "";
                }
                if( input.length()-1 != i && input.charAt(i+1) == ' ' )
                {
                    i++;
                }
            }    
        }
        System.out.println("OUT");
        InputDevice.setText("");
        
        switch( OS.realMachine.getDT() ){
            
            case 1 :
                for( int i = 0 ; i < words.length ; i++ ){
                    OS.rmMemory[OS.realMachine.getDB()*10+i].setCell(words[i]);
                }
                refreshRegisterFields();
                break;
            case 2 :
                for( int i = 0 ; i < words.length ; i++ ){
                    OS.externalMemory[OS.realMachine.getDB()*10+i].setCell(words[i]);
                }
                refreshRegisterFields();
                break;
            case 3 :
                for( int i = 0 ; i < words.length ; i++ )
                    Display.append( words[i]);
                break;
        }
            OS.cd.PerformTask = true;
        
        
    }
