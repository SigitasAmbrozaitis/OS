    public void fillRMmemory(){
        DefaultTableModel model = (DefaultTableModel) rmMemory.getModel();
        for( int i = 0 ; i < OS.getRM_MEMORY_SIZE() ; i++ ){
            model.addRow( new Object[]{ Integer.toString(i),OS.rmMemory[i].getCell()});
        }
    }
    
    public void fillExternalMemory(){
        DefaultTableModel model = (DefaultTableModel) externalMemory.getModel();
        for( int i = 0 ; i < OS.getEXTERNAL_MEMORY_SIZE() ; i++ ){
            model.addRow( new Object[]{ Integer.toString(i),OS.externalMemory[i].getCell()});
        }
    }
    
    public void fillVMmemory(){
        DefaultTableModel model = (DefaultTableModel) vmMemory.getModel();
        for( int i = 0 ; i < OS.getVM_MEMORY_SIZE() ; i++ ){
            model.addRow( new Object[]{ Integer.toString(i), virtualMachine.memory[i].getCell()});
        }
    }
    
    public void removeRMmemory(){
        
        for(int i = 0 ; i < OS.getRM_MEMORY_SIZE() ; i++ ){
            ((DefaultTableModel)rmMemory.getModel()).removeRow(0);
        }
        for(int i = 0 ; i < OS.getVM_MEMORY_SIZE() ; i++ ){
            ((DefaultTableModel)vmMemory.getModel()).removeRow(0);
        }
        for(int i = 0 ; i < OS.getEXTERNAL_MEMORY_SIZE() ; i++ ){
            ((DefaultTableModel)externalMemory.getModel()).removeRow(0);
        }
        
    }
    
